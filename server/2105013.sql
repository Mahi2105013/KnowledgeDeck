CREATE TABLE FLASHCARDS (
WORD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
WORD_STRING VARCHAR2(50) NOT NULL UNIQUE,
PARTS_OF_SPEECH VARCHAR2(15),
EXAMPLE_SENTENCE VARCHAR2(200),
MEANING VARCHAR2(1000) NOT NULL UNIQUE,
DIFFICULTY VARCHAR2(1) NOT NULL, -- E= EASY, D = DIFFICULT
CATEGORY VARCHAR2(15) NOT NULL, -- VOCAB, MATH FORMULAE, IMPORTANT DATES
);

CREATE TABLE DELETED_FLASHCARDS (
WORD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
WORD_STRING VARCHAR2(50) NOT NULL UNIQUE,
MEANING VARCHAR2(1000) NOT NULL UNIQUE,
DIFFICULTY VARCHAR2(1) NOT NULL, -- E= EASY, D = DIFFICULT
CATEGORY VARCHAR2(15) NOT NULL, -- VOCAB, MATH FORMULAE, IMPORTANT DATES
);

CREATE TABLE USERS (
USER_ID NUMBER NOT NULL,
USER_NAME VARCHAR2(255) NOT NULL,
USER_EMAIL VARCHAR2(255) NOT NULL,
USER_PASSWORD VARCHAR2(255) NOT NULL
);

CREATE TABLE BOOKMARKS (
BOOKMARK_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
WORD_ID NUMBER,
FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
FOREIGN KEY (WORD_ID) REFERENCES FLASHCARDS (WORD_ID)
);

CREATE TABLE NOTES (
NOTE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
WORD_ID NUMBER,
NOTE_BODY VARCHAR2(1000),
FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
FOREIGN KEY (WORD_ID) REFERENCES FLASHCARDS (WORD_ID)
);

CREATE TABLE COMMENTS (
COMMENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER NOT NULL,
DIFFICULTY VARCHAR2(1) NOT NULL,
COMMENT_BODY VARCHAR2(1000),
DATE_OF_COMMENT DATE DEFAULT SYSDATE,

FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
);

CREATE TABLE COMMENT_UPVOTING (
    COMMENT_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,

    FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
    FOREIGN KEY (COMMENT_ID) REFERENCES COMMENTS (COMMENT_ID),
    PRIMARY KEY (COMMENT_ID, USER_ID)
);



