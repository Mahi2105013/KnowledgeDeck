CREATE TABLE FLASHCARDS (
WORD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
WORD_STRING VARCHAR2(50) NOT NULL UNIQUE,
MEANING VARCHAR2(1000) NOT NULL UNIQUE,
DIFFICULTY VARCHAR2(1) NOT NULL, -- E= EASY, D = DIFFICULT
CATEGORY VARCHAR2(15) NOT NULL, -- VOCAB, MATH FORMULAE, IMPORTANT DATES
PARTS_OF_SPEECH VARCHAR2(15),
EXAMPLE_SENTENCE VARCHAR2(200),
CUSTOMBOOL NUMBER DEFAULT 0 -- [0 = NORMAL WORD, 1 = CUSTOM WORD]
USER_ID NUMBER DEFAULT 1 -- [1 = ADMIN'S USER_ID]

CONSTRAINT fk_user_flashcards FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
ON DELETE CASCADE
);

CREATE TABLE SECTION_WISE_WORDS (
WORD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
WORD_STRING VARCHAR2(50) NOT NULL UNIQUE,
MEANING VARCHAR2(100) NOT NULL UNIQUE,
CATEGORY VARCHAR2(30) NOT NULL, -- HEALTH, EDUCATION ETC
EXAMPLE_SENTENCE VARCHAR2(100)
);

CREATE TABLE SECTION_WISE_WORDS_NOTES_BOOKMARKS (
ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
WORD_ID NUMBER,
BOOKMARK_BOOL NUMBER DEFAULT 0, -- 1 = BOOKMARKED, 0 = NOT BOOKMARKED
NOTE_BODY VARCHAR2(1000),
CONSTRAINT fk_user_sec FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID) ON DELETE CASCADE,
CONSTRAINT fk_word_sec FOREIGN KEY (WORD_ID) REFERENCES FLASHCARDS (WORD_ID) ON DELETE CASCADE
);

CREATE TABLE DELETED_FLASHCARDS (
WORD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
WORD_STRING VARCHAR2(50) NOT NULL UNIQUE,
MEANING VARCHAR2(1000) NOT NULL UNIQUE,
DIFFICULTY VARCHAR2(1) NOT NULL, -- E= EASY, D = DIFFICULT
CATEGORY VARCHAR2(15) NOT NULL, -- VOCAB, MATH FORMULAE, IMPORTANT DATES
);

CREATE TABLE USERS (
USER_ID NUMBER NOT NULL,
USER_NAME VARCHAR2(255) NOT NULL,
USER_EMAIL VARCHAR2(255) NOT NULL,
USER_PASSWORD VARCHAR2(255) NOT NULL
);

CREATE TABLE BOOKMARKS (
BOOKMARK_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
WORD_ID NUMBER,
CONSTRAINT fk_user FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID) ON DELETE CASCADE,
CONSTRAINT fk_word FOREIGN KEY (WORD_ID) REFERENCES FLASHCARDS (WORD_ID) ON DELETE CASCADE
);

CREATE TABLE NOTES (
NOTE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER,
WORD_ID NUMBER,
NOTE_BODY VARCHAR2(1000),
LAST_EDITED DATE DEFAULT SYSDATE,
CONSTRAINT fk_user_notes FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID) ON DELETE CASCADE,
CONSTRAINT fk_word_notes FOREIGN KEY (WORD_ID) REFERENCES FLASHCARDS (WORD_ID) ON DELETE CASCADE
);

CREATE TABLE COMMENTS (
COMMENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
USER_ID NUMBER NOT NULL,
DIFFICULTY VARCHAR2(1) NOT NULL,
COMMENT_BODY VARCHAR2(1000),
DATE_OF_COMMENT DATE DEFAULT SYSDATE,

CONSTRAINT fk_user_comments FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID) ON DELETE CASCADE
);

